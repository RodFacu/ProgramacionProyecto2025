    private void registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registroActionPerformed
  // Cierra u oculta la ventana principal
    this.setVisible(false); 

    // Si no existe la ventana de registro, crearla
    if (registroWindow == null) {
        registroWindow = new PantallaRegistro("defecto");
    }

    // Mostrar la ventana de registro
    registroWindow.setVisible(true);

    }//GEN-LAST:event_registroActionPerformed

    private void informacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_informacionActionPerformed
                                             
    // Crear la ventana solo si no existe
    if (infoWindow == null) {
        infoWindow = new PantallaInformacion();
    }

    // Mostrar la ventana
    infoWindow.setVisible(true);

    // Ocultar la ventana principal
    this.setVisible(false);

    }//GEN-LAST:event_informacionActionPerformed

    private void infoadmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_infoadmActionPerformed
      // Método que se ejecuta al hacer clic en el botón o acción asociada a "Información Administración".
    // Su objetivo es abrir el diálogo de login antes de mostrar la ventana de administración.

    PantallaLogin loginDialog = new PantallaLogin(this);
    // Crea un nuevo diálogo de login, pasando 'this' como ventana padre para centrarlo.

    loginDialog.setVisible(true);
    // Muestra el diálogo de login de forma modal.
    // La ejecución de este método se detiene hasta que el usuario cierre el diálogo.

    dispose();
    // Cierra la ventana actual de PantallaPrincipal.
    // Esto asegura que al iniciar sesión, la ventana principal no quede abierta detrás del login.


    }//GEN-LAST:event_infoadmActionPerformed

