  private boolean accesoPermitido() {
    PantallaLogin loginDialog = new PantallaLogin(this); // pasa la ventana principal
    loginDialog.setVisible(true); // se bloquea hasta que se cierre el login
    return loginDialog.isLoginExitoso(); // devuelve true si el login fue exitoso
}


